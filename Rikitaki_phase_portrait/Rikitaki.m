u = 0.498;
a = 0.1;

[t, X] = ode45(@(t,X) pendulum_n(t,X,u,a), t_interval, Start_cond );
plot3(X(:,1),X(:,2),X(:,3))
hold off
% –ø–æ–ª—É—á–∞–µ–º –≥—Ä–∞—Ñ–∏–∫, –≥–¥–µ –≤–∏–¥–Ω–æ –∏ —Å–µ–¥–ª–æ –∏ —Å–ø–∏—Ä–∞–ª—å–Ω—ã–π —Ñ–æ–∫—É—Å

function dXdt = pendulum_n(t,X,u,a)
    dx = -1*u*X(1)+X(2)*X(3);
    dy = X(1)*(X(3)-a)-u*X(2);
    dz = 1 - X(1)*X(2);
    dXdt = [dx;dy;dz];
end
